export async function POST() {
  try {
    await db.article.deleteMany({});
    const allArticles = [...seedArticles, ...additionalArticles, ...extraArticles];
    const articles = await Promise.all(
      allArticles.map((article) =>
        db.article.create({
          data: {
            title: article.title,
            slug: article.slug,
            excerpt: article.excerpt,
            content: article.content,
            coverImage: article.coverImage,
            calculatorType: article.calculatorType,
          },
        })
      )
    );
    return NextResponse.json({
      success: true,
      count: articles.length,
      message: `تم إنشاء ${articles.length} مقالات بنجاح`,
    });
  } catch (error) {
    console.error('Seed error:', error);
    return NextResponse.json({ error: 'فشل في تهيئة البيانات' }, { status: 500 });
  }
}

export async function GET() {
  try {
    const count = await db.article.count();
    return NextResponse.json({ count });
  } catch (error) {
    console.error('Seed count error:', error);
    return NextResponse.json({ count: 0 });
  }
}
